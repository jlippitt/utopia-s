const Self = @This();

data: *[size]u8,
dirty: bool = false,

pub fn init(arena: *std.heap.ArenaAllocator, vfs: fw.Vfs) fw.Vfs.Error!Self {
    const data = try arena.allocator().alloc(u8, size);
    const read_len = try vfs.readSave(arena.allocator(), "mempak", data);

    if (read_len != size) {
        format(data[0..size]);
    }

    return .{
        .data = data[0..size],
    };
}

pub fn save(self: *Self, allocator: std.mem.Allocator, vfs: fw.Vfs) fw.Vfs.Error!void {
    if (!self.dirty) {
        return;
    }

    try vfs.writeSave(allocator, "mempak", self.data);
    self.dirty = false;
}

pub fn read(
    self: *const Self,
    channel: u32,
    recv_data: *std.ArrayListUnmanaged(u8),
    send_data: []const u8,
) error{OutOfMemory}!void {
    const start = (@as(u16, send_data[1]) << 8) | (send_data[2] & 0xe0);
    const end = start + block_size;

    const data = if (channel == 0 and end < self.data.len)
        self.data[start..][0..block_size]
    else
        &[1]u8{0} ** block_size;

    fw.log.debug("Mempak Read ({d}): {X:04} => {any} ({d})", .{
        channel,
        start,
        data,
        crc8(data),
    });

    try recv_data.appendSliceBounded(data);
    try recv_data.appendBounded(crc8(data));
}

pub fn write(
    self: *Self,
    channel: u32,
    recv_data: *std.ArrayListUnmanaged(u8),
    send_data: []const u8,
) error{OutOfMemory}!void {
    const start = (@as(u16, send_data[1]) << 8) | (send_data[2] & 0xe0);
    const data = send_data[3..][0..block_size];

    fw.log.debug("Mempak Write ({d}): {X:04} => {any} ({d})", .{
        channel,
        start,
        data,
        crc8(data),
    });

    if (channel == 0 and start < self.data.len) {
        @memcpy(self.data[start..][0..block_size], data);
        self.dirty = true;
    }

    try recv_data.appendBounded(crc8(data));
}

fn format(data: *[size]u8) void {
    const pages: *[num_pages][page_size]u8 = @ptrCast(data);

    @memset(data, 0);
    @memcpy(&pages[0], &id_sector);

    pages[1][1] = 0x71;
    pages[2][1] = 0x71;

    for (5..num_pages) |index| {
        pages[1][(index << 1) | 1] = 0x03;
        pages[2][(index << 1) | 1] = 0x03;
    }
}

fn crc8(data: *const [block_size]u8) u8 {
    var result: u8 = 0;

    for (0..(block_size + 1)) |index| {
        for (0..8) |bit| {
            const xor_tap: u8 = if (fw.num.bit(result, 7)) 0x85 else 0;

            result <<= 1;

            if (index < block_size and
                fw.num.bit(data[index], 7 - @as(u3, @intCast(bit))))
            {
                result |= 1;
            }

            result ^= xor_tap;
        }
    }

    return result;
}
